import Foundation

/// Enum for HTTP Method
enum HTTPMethod: String {
    case get     = "GET"
    case post    = "POST"
    case put     = "PUT"
    case patch   = "PATCH"
    case delet   = "DELETE"
    case head    = "HEAD"
    case options = "OPTIONS"
}
